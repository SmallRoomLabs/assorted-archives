#include <Arduino.h>
#include "driver_common.h"

static BYTE8 isInitialised = 0;

// *******************************************************************************************************************************
//												Arduino Serial Out
// *******************************************************************************************************************************

void DRVWrite(BYTE8 command,DRVPARAM data) {
	if (isInitialised == 0) {
		Serial.begin(9600);
		isInitialised = 1;
	}
	switch(command) {
		case DWA1_WRITE:	Serial.print((char)data);break;
		case DWA1_BACKSPACE:Serial.print((char)8);break;
		case DWA1_NEWLINE:	Serial.print("\n");break;
	}
}

